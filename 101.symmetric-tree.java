/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    
    public boolean isSymmetric(TreeNode root) {
        if (root == null)
            return true;
        return compareLR(root.left, root.right);
    }
    private boolean compareLR(TreeNode l, TreeNode r){
        if (l == null ^ r == null)
            return false;
        if (l == null)
            return true;
        if (l.val != r.val)
            return false;
        return compareLR(l.left, r.right) && compareLR(l.right, r.left);
    }
}
